import time
import threading
import tkinter as tk
from tkinter import messagebox

# Function to show a message box
def show_message(title, message):
    def task():
        win = tk.Tk()
        win.withdraw()
        messagebox.showinfo(title, message)
        win.destroy()
    threading.Thread(target=task).start()

